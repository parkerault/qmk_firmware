/*
  SS_TAP() presses and releases a key.
  SS_DOWN() presses (but does not release) a key.
  SS_UP() releases a key.

  SEND_STRING("VE"SS_TAP(X_HOME)"LO");
  note how the prefix is now X_, and not KC_
*/

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
        case ARROW:
          if (record->event.pressed) {
            SEND_STRING("=>");
          }
          return false;
          break;
        case SEQ:
          if (record->event.pressed) {
            SEND_STRING("===");
          }
          return false;
          break;
        case NSEQ:
          if (record->event.pressed) {
            SEND_STRING("!==");
          }
          return false;
          break;
        case LTE:
          if (record->event.pressed) {
            SEND_STRING("<=");
          }
          return false;
          break;
        case GTE:
          if (record->event.pressed) {
            SEND_STRING(">=");
          }
          return false;
          break;
        case NFN:
          if (record->event.pressed) {
            SEND_STRING("nfn\n");
          }
          return false;
          break;
      }
    return true;
};
